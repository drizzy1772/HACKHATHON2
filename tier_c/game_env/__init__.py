# -*- coding: utf-8 -*-
"""
Ядро фреймворку хакатону «Політ крізь ліс».

Пакет тримає всю логіку симуляції, а `solution.py` (файл учасника) бачить лише
вузький контракт: (lidar_data, current_position, next_checkpoint) → (vx, vy, vz).
Глобальна мапа (дерева, рельєф, чекпойнти) для коду учасника прихована.

Тут зібрано ЄДИНЕ джерело сталих світу й симуляції (`SimConfig`), щоб рельєф,
генератор, симулятор і аналітика користувалися однаковими межами, стелею та
геометрією LiDAR. Одиниці SI, система координат правостороння: X вперед, Y вліво,
Z вгору (збігається з рушієм `src/drone_sim.py` і з Blender).
"""

from dataclasses import dataclass, field


@dataclass(frozen=True)
class SimConfig:
    """Сталі світу та сенсора. Незмінні під час прогону — щоб грейдинг був чесним."""

    # ── Межі світу й рельєф ───────────────────────────────────────────────────
    bounds: float = 25.0          # півсторона квадратної арени, м (арена 50×50 м)
    grid_res: int = 64            # роздільність heightmap-сітки (grid_res×grid_res вузлів)
    terrain_amp: float = 2.5      # амплітуда пагорбів (розмах висот рельєфу), м
    terrain_scale: float = 0.06   # просторова частота шуму Перліна (менше → пологіші пагорби)
    terrain_octaves: int = 4      # шари шуму (деталізація рельєфу)

    # ── Ліс ───────────────────────────────────────────────────────────────────
    n_trees: int = 60             # цільова кількість дерев
    tree_r_min: float = 0.15      # мін. радіус стовбура, м
    tree_r_max: float = 0.35      # макс. радіус стовбура, м
    tree_h_min: float = 4.0       # мін. висота дерева, м (вищий ліс → вища стеля)
    tree_h_max: float = 9.0       # макс. висота дерева, м
    tree_margin: float = 0.7      # мін. проміжок між кронами дерев, м (без перетину)

    # ── Чекпойнт-фура (бензовоз) з напівпрозорим циліндром зарахування ──────────
    # Усі сценарії/стадії прибрано — РІВНО ОДНА фіксована ціль на протилежному куті
    # арени від старту (та сама діагональ); маркер — модель бензовоза, зарахування —
    # за ПРОЛІТ крізь напівпрозорий вертикальний циліндр навколо неї.
    n_checkpoints: int = 1        # завжди 1 (фіксована ціль, не рандомна; не конфігурується)
    cp_radius: float = 1.2        # радіус наземного світного кільця-маркера, м (візуал)
    # Циліндр зарахування (XY-радіус/висота) МАЄ вміщати всю модель автоцистерни
    # (truck_len × truck_width = 5.0 × 2.5 м, півдіагональ ≈ 2.8 м) з запасом —
    # інакше фура візуально «стирчить» за межі напівпрозорого маяка-проліту.
    cp_cyl_radius: float = 5.2    # радіус циліндра зарахування (XY), м (> півдіагоналі фури)
    cp_cyl_height: float = 8.0    # висота циліндра над основою фури, м (проліт у межах → OK)
    cp_clearance: float = 3.2     # висота центру чекпойнта над рельєфом, м
    cp_tree_clear: float = 2.5    # мін. відстань чекпойнта від будь-якого дерева, м
    # truck_len/truck_width — РЕАЛЬНІ виміряні розміри assets/checkpoint_truck_source.glb
    # (bmesh-аналіз офлайн; раніше тут стояли довільні 8.0×3.5 м, які НЕ відповідали
    # мешу взагалі — хітбокс був на 60%/40% більшим за видиму машину). Якір
    # пропорцій усіх моделей — дрон (assets/drone_source.glb, ~0.40 м, PLANE_SCALE
    # та інші множники підганяються під нього, не під фуру).
    truck_len: float = 5.0        # габарит фури: довжина, м
    truck_width: float = 2.5      # габарит фури: ширина, м
    truck_height: float = 2.7     # габарит фури: висота (кабіна+цистерна), м

    # ── Тематичні перешкоди (український контекст) ──────────────────────────────
    # Колізійні (LiDAR бачить, дотик → COLLISION): «Потужноліт», кришка бензобака.
    # Декоративні орієнтири (LiDAR ПРОХОДИТЬ крізь них, зіткнення немає): пес
    # Патрон, дороговказ «Чорнобаївка». Розкладка — детермінована за насінням.
    n_obstacles: int = 3          # колізійних тематичних перешкод на арені
    obstacle_clear: float = 2.5   # мін. відстань перешкоди від старту/чекпойнтів, м

    # ── Дрон і обмеження польоту ────────────────────────────────────────────────
    # СТЕЛЯ = СЕРЕДНЯ висота крон дерев (обчислюється на мапі, поле нижче — лише
    # абсолютний запобіжник, якщо дерев немає). Переліт стелі → «Під дією РЕБ»
    # (білий шум + втрата зв'язку) і негайна DISQUALIFICATION — жорсткий провал.
    ceiling: float = 15.0         # абсолютний запобіжник стелі, м (ефективну дає мапа —
                                  # середня висота крон, тепер вищих → стеля вища)
    drone_radius: float = 0.35    # радіус зіткнення корпусу, м (min(lidar) < r → COLLISION)
    start_clearance: float = 2.0  # висота старту над рельєфом, м

    # ── LiDAR (3D сферичний) ────────────────────────────────────────────────────
    lidar_n_az: int = 24          # променів по азимуту (навколо, 360°)
    lidar_n_el: int = 7           # кілець за висотою (елевація від −90° до +90°)
    lidar_range: float = 8.0      # МАКС. дальність променя, м (обмежений сенсор: далі
                                  # нічого не «видно» → повертається саме це значення)
    lidar_r_start: float = 0.36   # зсув початку променя від центра, м (> drone_radius, щоб не бити себе)

    # ── Керування / інтеграція ──────────────────────────────────────────────────
    # Ручний режим — БАЗОВИЙ 6-DOF рушій (керування швидкістю). Окремий РЕАЛІСТИЧНИЙ
    # рушій (angle-mode) — у розробницькій пісочниці sandbox/ (запуск власною командою).
    control_hz: float = 30.0      # частота виклику контролера учасника, Гц
    max_speed: float = 4.0        # обмеження модуля командної швидкості, м/с
    leash: float = 2.5            # повідець setpoint↔дрон (анти-windup PID), м
    timeout_s: float = 90.0       # ліміт часу прогону, с → TIMEOUT

    # ── Детермінізм ──────────────────────────────────────────────────────────────
    default_seed: int = 2026      # насіння змагальної мапи (для генерації-за-замовчуванням)

    @property
    def lidar_count(self) -> int:
        """Довжина вектора lidar_data (кількість променів сферичної сітки)."""
        return self.lidar_n_az * self.lidar_n_el


# Статуси прогону (машина станів симулятора)
STATUS_RUNNING = "RUNNING"
STATUS_FINISHED = "FINISHED"        # усі чекпойнти пройдено
STATUS_COLLISION = "COLLISION"      # зіткнення з деревом/рельєфом/перешкодою
STATUS_DISQUALIFIED = "DISQUALIFIED"  # перевищено стелю (переліт → РЕБ)
STATUS_TIMEOUT = "TIMEOUT"          # вичерпано ліміт часу

# Драматичні повідомлення оверлея за статусом (українською, вимога ТЗ).
# COLLISION → «Борт втрачено»; DISQUALIFIED (переліт стелі) → «Під дією РЕБ».
STATUS_MESSAGES = {
    STATUS_COLLISION: "БОРТ ВТРАЧЕНО",
    STATUS_DISQUALIFIED: "ПІД ДІЄЮ РЕБ",
    STATUS_FINISHED: "МІСІЮ ВИКОНАНО",
    STATUS_TIMEOUT: "ЧАС ВИЧЕРПАНО",
}


# ── Стадії (машина станів меню) ──────────────────────────────────────────────────
@dataclass(frozen=True)
class Stage:
    """Навчальна стадія. A/B — точка-в-точку (1 фура), C/D — 5 фур.
      • order_flexible=True → чекпойнти в ДОВІЛЬНОМУ порядку (найближчий непройдений,
        checkpoint_mode='nearest'); False → строго по черзі (для 1 фури це байдуже);
      • optimize=True → задача на ОПТИМІЗАЦІЮ маршруту (оцінка за шлях/час; B, D)."""
    key: str
    title: str
    n_checkpoints: int
    order_flexible: bool
    optimize: bool
    description: str

    @property
    def checkpoint_mode(self) -> str:
        return "nearest" if self.order_flexible else "sequential"


